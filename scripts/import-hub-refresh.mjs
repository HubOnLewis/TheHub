#!/usr/bin/env node
/**
 * HuB on Lewis Perfect Venue refresh import — multi-folder pipeline.
 *
 * Usage:
 *   npm run import:hub-refresh:audit
 *   npm run import:hub-refresh:dry-run
 *   npm run import:hub-refresh:apply
 *   node scripts/import-hub-refresh.mjs --root "C:\...\import" --tenant hub-on-lewis --apply
 */

import { MongoClient } from 'mongodb';
import { mkdirSync, writeFileSync, existsSync } from 'fs';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';
import { loadEnv, parseArgs, mapPvToDealStatus, ROOT, getMongoDb, parseMongoTarget } from './lib/hub-refresh-utils.mjs';
import { runRefreshPipeline } from './lib/hub-refresh-pipeline.mjs';
import { resolveHubTenantIds, resolvePrimaryHubTenant } from './lib/hub-tenant-resolve.mjs';

const __dirname = dirname(fileURLToPath(import.meta.url));
const OUT_DIR = resolve(ROOT, 'data/perfect-venue-processed/hub-refresh');
const WEB_MANIFEST_TS = resolve(ROOT, 'packages/web/src/data/hubRefreshManifest.ts');
const WEB_EVENTS_TS = resolve(ROOT, 'packages/web/src/data/hubRefreshEvents.ts');

loadEnv();

const { root, tenant, apply, audit, dryRun, confirmProduction } = parseArgs(process.argv.slice(2));
const tenantIds = resolveHubTenantIds(tenant);
const primaryTenant = resolvePrimaryHubTenant(tenant);
const importBatchId = `hub-refresh-${new Date().toISOString().replace(/[:.]/g, '-').slice(0, 19)}`;

console.log('\n[hub-refresh] Perfect Venue refresh import');
console.log(`[hub-refresh] Mode: ${apply ? 'APPLY' : audit ? 'AUDIT' : 'DRY-RUN'}`);
console.log(`[hub-refresh] Root: ${root}`);
console.log(`[hub-refresh] Tenant: ${primaryTenant} (scope: ${tenantIds.join(', ')})`);

if (!existsSync(root)) {
  console.error(`[hub-refresh] Import root not found: ${root}`);
  process.exit(1);
}

const result = runRefreshPipeline({ root, importBatchId });
const { summary, events, contacts, payments, documents, unmatched, warnings, contamination, discovered } =
  result;

console.log('\n[hub-refresh] Folders detected:');
for (const [folder, info] of Object.entries(summary.folders)) {
  console.log(`  ${folder}: ${info.exists ? `${info.fileCount} file(s)` : 'missing'}`);
}

console.log('\n[hub-refresh] Parse summary:');
console.log(`  Events (master):     ${summary.eventsParsed}`);
console.log(`  Unique PV IDs:       ${summary.uniquePvIds}`);
console.log(`  Contacts:            ${summary.contactsParsed}`);
console.log(`  Payments parsed:     ${summary.paymentsParsed} (${summary.paymentsMatched} matched)`);
console.log(`  Documents matched:   ${summary.documentsMatched}`);
console.log(`  Invoices matched:    ${summary.invoicesMatched}`);
console.log(`  BEOs matched:        ${summary.beosMatched}`);
console.log(`  Agreements matched:  ${summary.agreementsMatched}`);
console.log(`  Menus matched:       ${summary.menusMatched}`);
console.log(`  Unmatched:           ${summary.unmatchedCount}`);
console.log(`  Warnings:            ${summary.warningsCount}`);
console.log(`  Contamination hits:  ${summary.contaminationCount}`);

if (summary.duplicatePvIds.length) {
  console.warn(`  Duplicate PV IDs:    ${summary.duplicatePvIds.join(', ')}`);
}

if (contamination.length) {
  console.error('\n[hub-refresh] CONTAMINATION DETECTED — import blocked.');
  for (const c of contamination.slice(0, 20)) {
    console.error(`  - ${c.title ?? c.payer ?? c.eventId}: matched "${c.term}"`);
  }
  process.exit(1);
}

mkdirSync(OUT_DIR, { recursive: true });
const writeArtifacts = () => {
  writeFileSync(resolve(OUT_DIR, 'events.normalized.json'), JSON.stringify(events, null, 2));
  writeFileSync(resolve(OUT_DIR, 'contacts.normalized.json'), JSON.stringify(contacts, null, 2));
  writeFileSync(resolve(OUT_DIR, 'payments.normalized.json'), JSON.stringify(payments, null, 2));
  writeFileSync(resolve(OUT_DIR, 'documents.normalized.json'), JSON.stringify(documents, null, 2));
  writeFileSync(resolve(OUT_DIR, 'unmatched-records.json'), JSON.stringify(unmatched, null, 2));
  writeFileSync(resolve(OUT_DIR, 'warnings.json'), JSON.stringify(warnings, null, 2));
  writeFileSync(resolve(OUT_DIR, 'import-summary.json'), JSON.stringify(summary, null, 2));

  const manifest = { ...summary, contaminationAudit: 'pass' };

  writeFileSync(
    WEB_MANIFEST_TS,
    `/** Auto-generated by scripts/import-hub-refresh.mjs — do not edit manually. */

export type HubRefreshManifest = {
  importBatchId: string;
  importedAt: string;
  sourceRoot: string;
  eventsParsed: number;
  uniquePvIds: number;
  contactsParsed: number;
  paymentsParsed: number;
  paymentsMatched: number;
  documentsMatched: number;
  invoicesMatched: number;
  beosMatched: number;
  agreementsMatched: number;
  menusMatched: number;
  eventSummariesMatched: number;
  staffBeosMatched: number;
  unmatchedCount: number;
  warningsCount: number;
  contaminationCount: number;
  contaminationAudit: string;
  duplicatePvIds: string[];
  financialTotals: { grandTotal: number; amountPaid: number; balanceDue: number };
  byStatus: Record<string, number>;
  folders: Record<string, { exists: boolean; fileCount: number }>;
  importCommands?: { audit: string; dryRun: string; apply: string };
};

export const HUB_REFRESH_AVAILABLE = true;

export const HUB_REFRESH_MANIFEST: HubRefreshManifest = ${JSON.stringify(manifest, null, 2)};
`,
  );

  writeFileSync(
    WEB_EVENTS_TS,
    `/** Auto-generated by scripts/import-hub-refresh.mjs — do not edit manually. */

import type { HubRefreshEvent } from './hubRefreshTypes.js';

export const HUB_REFRESH_EVENTS: HubRefreshEvent[] = ${JSON.stringify(events, null, 2)};
`,
  );
};

if (audit) {
  console.log('\n[hub-refresh] Audit complete (no writes).');
  for (const f of discovered.files) {
    console.log(`  [${f.role}] ${f.name}`);
  }
  process.exit(0);
}

writeArtifacts();
console.log(`\n[hub-refresh] Processed artifacts: ${OUT_DIR}`);

if (dryRun && !apply) {
  const active = events.filter(e => e.pvStatus !== 'lost' && e.pvStatus !== 'completed');
  console.log(`\n[hub-refresh] Would upsert ${events.length} event(s) (${active.length} active pipeline)`);
  console.log(`[hub-refresh] Would upsert ${payments.length} payment record(s)`);
  console.log('\n[hub-refresh] Dry-run complete. Re-run with --apply to import to Mongo.');
  process.exit(0);
}

// ── Backup + Mongo apply ───────────────────────────────────────────
const uri = process.env.MONGODB_URI;
if (!uri) {
  console.error('[hub-refresh] MONGODB_URI required for --apply');
  process.exit(1);
}

const mongoTarget = parseMongoTarget(uri);
console.log(`\n[hub-refresh] Mongo target: host=${mongoTarget.host} db=${mongoTarget.dbName}`);
console.log(`[hub-refresh] Upsert target tenant: ${primaryTenant}`);
console.log(`[hub-refresh] Records to upsert: ${events.length} deal(s), ${payments.length} payment(s)`);

if (!confirmProduction) {
  console.error(
    '[hub-refresh] APPLY blocked — re-run with --apply --confirm-production after verifying Mongo target above.',
  );
  process.exit(1);
}

if (mongoTarget.dbName !== 'hub_crm') {
  console.warn(
    `[hub-refresh] WARNING: DB name is "${mongoTarget.dbName}" — production Render API uses hub_crm.`,
  );
}

const stamp = new Date().toISOString().replace(/[:.]/g, '-').slice(0, 19);
const backupDir = resolve(ROOT, 'data/backups', `${primaryTenant}-pre-refresh-${stamp}`);
mkdirSync(backupDir, { recursive: true });

const client = new MongoClient(uri);
let created = 0;
let updated = 0;
let paymentsUpserted = 0;

try {
  await client.connect();
  const db = getMongoDb(client);
  const dealsCol = db.collection('deals');
  const paymentsCol = db.collection('hub_payments');

  const existing = await dealsCol.find({ tenantId: { $in: tenantIds } }).toArray();
  writeFileSync(resolve(backupDir, 'deals-backup.json'), JSON.stringify(existing, null, 2));
  const existingPayments = await paymentsCol.find({ tenantId: primaryTenant }).toArray();
  writeFileSync(resolve(backupDir, 'payments-backup.json'), JSON.stringify(existingPayments, null, 2));
  console.log(`[hub-refresh] Backup: ${backupDir}`);

  const now = new Date();
  for (const e of events) {
    const existingDeal = await dealsCol.findOne({
      tenantId: primaryTenant,
      'importMeta.sourceKey': e.sourceKey,
    });

    const doc = {
      tenantId: primaryTenant,
      title: e.title,
      company: e.company || e.contact,
      contact: e.contact,
      amount: e.grandTotal,
      assignedTo: e.owner || undefined,
      status: mapPvToDealStatus(e.pvStatus),
      notes: [
        e.leadSource ? `Lead source: ${e.leadSource}` : '',
        e.enrichmentNotes.join('\n'),
      ]
        .filter(Boolean)
        .join('\n')
        .trim() || undefined,
      source: 'perfect_venue_refresh',
      importBatchId,
      importMeta: {
        source: 'perfect_venue_refresh',
        sourceKey: e.sourceKey,
        pvId: e.pvId,
        pvEventId: e.pvEventId,
        pvStatus: e.pvStatus,
        eventDateIso: e.eventDateIso,
        startTime: e.startTime,
        endTime: e.endTime,
        guests: e.guests,
        space: e.space,
        contactEmail: e.contactEmail,
        contactPhone: e.contactPhone,
        subtotal: e.subtotal,
        grandTotal: e.grandTotal,
        amountPaid: e.amountPaid,
        balanceDue: e.balanceDue,
        depositPaid: e.depositPaid,
        paidInFull: e.paidInFull,
        paymentCount: e.paymentCount,
        latestPaymentDate: e.latestPaymentDate,
        documents: e.documents,
        documentFiles: e.documentFiles,
        sourceFiles: { master: e.sourceFile },
        payments: e.payments,
      },
      updatedAt: now,
      lastTouchedAt: e.lastContactedIso ? new Date(e.lastContactedIso) : now,
    };

    if (existingDeal) {
      await dealsCol.updateOne({ _id: existingDeal._id }, { $set: doc });
      updated += 1;
    } else {
      await dealsCol.insertOne({
        ...doc,
        createdAt: e.createdOnIso ? new Date(e.createdOnIso) : now,
      });
      created += 1;
    }
  }

  for (const p of payments) {
    await paymentsCol.updateOne(
      { tenantId: primaryTenant, id: p.id },
      {
        $set: { ...p, tenantId: primaryTenant, updatedAt: now },
        $setOnInsert: { createdAt: now },
      },
      { upsert: true },
    );
    paymentsUpserted += 1;
  }

  const archived = await dealsCol.updateMany(
    {
      tenantId: primaryTenant,
      'importMeta.source': { $exists: true, $ne: 'perfect_venue_refresh' },
    },
    {
      $set: {
        status: 'Lost',
        updatedAt: now,
        notes: '[archived-pre-refresh-import]',
      },
    },
  );

  summary.mongo = {
    created,
    updated,
    paymentsUpserted,
    archivedStale: archived.modifiedCount,
    backupDir,
  };
  writeFileSync(resolve(OUT_DIR, 'import-summary.json'), JSON.stringify(summary, null, 2));

  console.log(`\n[hub-refresh] APPLY complete.`);
  console.log(`  Deals created:  ${created}`);
  console.log(`  Deals updated:  ${updated}`);
  console.log(`  Payments upserted: ${paymentsUpserted}`);
} finally {
  await client.close();
}
